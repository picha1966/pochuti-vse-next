export interface FAQItem {
  question: string;
  answer: string;
}

export function getProductFAQs(productTitle: string, priceFrom?: string): FAQItem[] {
  const name = productTitle.replace(/^Слуховий апарат\s+/i, '');
  const price = priceFrom ? `від ${priceFrom} грн` : 'за запитом';

  return [
    {
      question: `Чи підійде ${name} для людини похилого віку?`,
      answer: `Так, більшість завушних моделей Signia та Audio Service розроблені з урахуванням зручності для людей похилого віку: великі кнопки керування, проста зміна батарейок та зрозуміла регулювання гучності. Наш фахівець допоможе налаштувати апарат під індивідуальні потреби та навчить правильному догляду.`,
    },
    {
      question: `Скільки коштує ${name}?`,
      answer: `Орієнтовна вартість — ${price}. Точна ціна залежить від індивідуальних потреб і визначається після діагностики слуху. Можлива розстрочка та державна компенсація для осіб з інвалідністю. Зателефонуйте нам для консультації.`,
    },
    {
      question: `Яка гарантія на ${name}?`,
      answer: `Усі апарати надходять з офіційною гарантією виробника. Ми також забезпечуємо безкоштовне налаштування та сервісне обслуговування протягом усього терміну служби. Гарантійний та позагарантійний ремонт виконується у нашому центрі у Вінниці та Хмельницькому.`,
    },
    {
      question: `Чи є безкоштовний пробний період для ${name}?`,
      answer: `Ми пропонуємо первинне налаштування апарату без зобов'язань. Ви зможете оцінити, як апарат звучить і сидить, ще до прийняття рішення про придбання. Зателефонуйте нам для консультації та приходьте разом з тими, хто часто з вами спілкується — це допоможе точніше налаштувати апарат.`,
    },
    {
      question: `Чи можна отримати державну компенсацію на ${name}?`,
      answer: `Так. Особи з підтвердженою інвалідністю по слуху в Україні мають право на державну компенсацію вартості слухового апарату. Наші фахівці допоможуть оформити всі необхідні документи та направлення. Зверніться до нас за консультацією.`,
    },
  ];
}
